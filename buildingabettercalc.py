##More advanced calculator
num1 = float(input("Enter 1st Number: "))
num2 = float(input("Enter 2nd Number: "))
op = input("Enter Operator: ")

if op == "+":
    print(num1 + num2)
elif op == "-":
    print(num1 - num2)
elif op == "*":
    print(num1 * num2)
elif op == "/":
    print(num1 / num2)
elif op == "%":
    print(num1 % num2)
else:
    print("XD..It's Invalid")
 